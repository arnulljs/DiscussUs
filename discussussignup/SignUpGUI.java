    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        // Retrieve input data
        String username = jTextField4.getText().trim();
        String email = jTextField3.getText().trim();
        String password = new String(jPasswordField2.getPassword()).trim();

        // Validate user input
        if (username.isEmpty() || email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields are required!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!email.contains("@") || !email.contains(".")) {
            JOptionPane.showMessageDialog(this, "Invalid email format!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (password.length() < 6) {
            JOptionPane.showMessageDialog(this, "Password must be at least 6 characters long.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Interact with UserDAO
        UserDAO userDAO = new UserDAO();
        try {
            boolean success = userDAO.insertUser(username, email, password);

            if (success) {
                JOptionPane.showMessageDialog(this, "Sign Up successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
                
             // Optionally switch to login GUI if implemented
                this.dispose();
                SignInGUI login = new SignInGUI();
                login.setVisible(true);
                login.pack();
                login.setLocationRelativeTo(null);
                
                /*jTextField4.setText(""); // Clear username field
                jTextField3.setText(""); // Clear email field
                jPasswordField2.setText(""); // Clear password field*/
            } else {
                JOptionPane.showMessageDialog(this, "Sign Up failed. User may already exist.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "An error occurred during sign-up: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

    
   
