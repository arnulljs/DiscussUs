     private void loadComments() {
        // Update the table with the current post's comments
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear the table
        for (Comment comment : currentPost.getComments()) {
            model.addRow(new Object[]{comment.getText(), comment.getDate()});
        }
    }
     
      private void updateCommentsTable() {
        // Refresh the table after adding a new comment
        loadComments();
    }
     
     
