     private void addCommentToTable(Comment comment) {
        // Create a 2D array for JTable data (just one column in this case)
        Object[][] data = new Object[commentsList.size()][1];

        // Populate the data array with comment texts
        for (int i = 0; i < commentsList.size(); i++) {
            data[i][0] = commentsList.get(i).getText();
        }

        // Set the new data into the table
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            data,
            new String[] {"Comments"}
        ));
    }
