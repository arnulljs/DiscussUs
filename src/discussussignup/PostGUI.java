    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      // Access the file path from the currentPost
        String filePath = post.getFilePath(); // Assuming Post has getFilePath() method

        if (filePath != null) {
            // Open the file using Desktop API
            try {
                File file = new File(filePath);
                if (file.exists()) {
                    Desktop.getDesktop().open(file); // This will open the file with the default application
                } else {
                    JOptionPane.showMessageDialog(null, "File does not exist.");
                }
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Error opening file: " + ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "No file path associated with this post.");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

