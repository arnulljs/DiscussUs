    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        
         String selectedForum = (String) jComboBox1.getSelectedItem();

    // Add the post to the appropriate forum based on selection
    if ("CPEA 2101".equals(selectedForum)) {
        this.dispose();
        // Instantiate and show the CPEA 2101 Forum GUI
        CPEA2101ForumGUI circuitsforum = new CPEA2101ForumGUI();
        circuitsforum.setVisible(true);
        circuitsforum.pack();
        circuitsforum.setLocationRelativeTo(null);
    } else if ("CPE 2101L".equals(selectedForum)) {
        this.dispose();
        // Instantiate and show the OOP Forum GUI
        OOPForumGUI oopforum = new OOPForumGUI();
        oopforum.setVisible(true);
        oopforum.pack();
        oopforum.setLocationRelativeTo(null);
    } else if ("EM-DECPE".equals(selectedForum)) {
        this.dispose();
        // Instantiate and show the DE Forum GUI
        DEForumGUI deforum = new DEForumGUI();
        deforum.setVisible(true);
        deforum.pack();
        deforum.setLocationRelativeTo(null);
    }
    }//GEN-LAST:event_jButton12ActionPerformed

