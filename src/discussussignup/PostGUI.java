    private void displayPostDetails() {
    if (post != null) {
        String title = post.getTitle();  // Assuming getTitle() method exists
        if (title != null && !title.isEmpty()) {
            jLabel1.setText(title);  // Display title in jTextArea3
        } else {
            jLabel1.setText("No Title");  // Placeholder in case title is null or empty
        }
        
        String content = post.getContent();  // Assuming getContent() method exists
        if (content != null && !content.isEmpty()) {
            jTextArea2.setText(content);  // Display content in jTextArea2
        } else {
            jTextArea2.setText("No Content");  // Placeholder in case content is null or empty
        }
    } else {
        jLabel1.setText("Post is null");  // Handle case where the post is null
        jTextArea2.setText("Content is unavailable");
    }
}
